package com.tek.general;

public class UsingIf {
	
	private int a=10;
	private int b=20;

	public UsingIf() {
		 
		 
		 
		 if(a+b==30)
		 {
			 System.out.println("The sum of two numbers is 30");
			 
		 }
		 if(a*b==300)
		 {
			 System.out.println("The product of two numbers is 300");
		 }
		
	}

	public static void main(String[] args) {
		
		new UsingIf();
		

	}

}
